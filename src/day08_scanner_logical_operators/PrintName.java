package day08_scanner_logical_operators;

public class PrintName {
    public static void main(String[] args) {

        String name = "Tom Jerry"; // We assigned Data HardCoded way
        System.out.println("Hello, " + name);  // Using variableName DYNAMICALLY
        System.out.println("Hello, " + "Tom Jerry"); // Using "Tom Jerry" STATICALLY/Hard Coded way

        System.out.println("What is you name: ");
        System.out.println("My name is: " + name );

    }
}
